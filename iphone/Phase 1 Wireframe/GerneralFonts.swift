        return UIFont(name: "Roboto-Regular", size: 12.0)!
    }
    
    public class var TEXTFIELD: UIFont {
        return UIFont(name: "Roboto-Bold", size: 14.0)!
    }
    
    public class var LOGO: UIFont {
        return UIFont(name: "Roboto-Black", size: 36.0)!
    }
    
}
